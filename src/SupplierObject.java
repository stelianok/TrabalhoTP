class SupplierObject {
  public int id;
  public String name;
  public String address;
  public String phone;
  public String email;

  public SupplierObject(int id, String name, String address, String phone, String email) {
    this.id = id;
    this.name = name;
    this.address = address;
    this.phone = phone;
    this.email = email;
  }
}